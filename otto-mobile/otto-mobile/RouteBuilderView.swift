struct RouteBuilderView: View {
    let initialCenter: CLLocationCoordinate2D
    let initialMapViewport: Viewport
    let onRouteSaved: ((SavedRouteDTO) -> Void)?

    @Environment(\.dismiss) private var dismiss
    @EnvironmentObject private var appState: AppState
    @EnvironmentObject private var locationService: LocationService
    @StateObject private var perfDiagnostics = RouteBuilderPerfDiagnostics()
    @AppStorage(OttoDebugSettings.routeBuilderPerfOverlayKey) private var routeBuilderPerfOverlayEnabled = false
    @State private var editingRouteId: String? = nil
    @State private var programmaticMapViewport: Viewport?
    @State private var mapRegionCache: RouteBuilderMapRegionCache
    @State private var mapContainerSize: CGSize = .zero
    @State private var mapBottomSafeArea: CGFloat = 0
    @State private var measuredBottomBarMinY: CGFloat?
    @State private var routeName = "New Route"
    @State private var points: [RouteBuilderPoint] = []
    @State private var roadCoordinates: [CLLocationCoordinate2D] = []
    @State private var roadPolylineIndex: RoutePolylineIndex?
    @State private var mapMarkerLODTier: RouteBuilderMapMarkerLODTier = .street
    @State private var mapVisibleLatitudeDelta: Double = 0.008
    @State private var mapCameraDebounceTask: Task<Void, Never>?
    @State private var distanceMeters: Double = 0
    @State private var travelSeconds: TimeInterval = 0
    @State private var isSnapping = false
    @State private var didSnapToRoad = false
    @State private var isSaving = false
    @State private var isShowingRouteNamePrompt = false
    @State private var markerActionPointID: UUID?
    @State private var movingPointID: UUID?
    @State private var routePointPendingDeleteID: UUID?
    @State private var routeNameDraft = ""
    @State private var errorMessage: String?
    @State private var snapTask: Task<Void, Never>?
    @State private var polylineIndexTask: Task<Void, Never>?
    @State private var autoPathBootstrapTask: Task<Void, Never>?
    @State private var editBaseline: RouteEditBaseline?
    @State private var lastSnapTurnCoordinates: [CLLocationCoordinate2D] = []
    @State private var isManualMode = false
    @State private var isEditMode = false
    @State private var isRunningGuidedGeneration = false
    @State private var hasCompletedGuidedGeneration = false
    @State private var guidedBackToStartStep = false
    @State private var selectedCheckpointDensity: CheckpointDensityTier = .recommended
    @State private var activeCheckpointSpacingMeters: Double?
    @State private var pendingCheckpointRegeneration: PendingCheckpointRegeneration?
    @State private var editUndoStack: [RouteBuilderEditSnapshot] = []
    @State private var isShowingDiscardChangesAlert = false
    @State private var showRouteEducation = false
    @State private var showLocationPrimer = false
    @State private var didApplyUserLocationRecenter = false
    @State private var latestObservedCameraRegion: MKCoordinateRegion

    init(initialCenter: CLLocationCoordinate2D, onRouteSaved: ((SavedRouteDTO) -> Void)? = nil) {
        self.initialCenter = initialCenter
        self.onRouteSaved = onRouteSaved
        let region = MKCoordinateRegion(
            center: initialCenter,
            span: Self.newRouteMapSpan
        )
        self.initialMapViewport = OttoMapboxCamera.viewport(for: region)
        _mapRegionCache = State(initialValue: RouteBuilderMapRegionCache(region: region))
        _latestObservedCameraRegion = State(initialValue: region)
        _mapVisibleLatitudeDelta = State(initialValue: Self.markerLODLatitudeDelta(for: region))
        _mapMarkerLODTier = State(
            initialValue: RouteBuilderMapMarkerLODTier.from(
                latitudeDelta: Self.markerLODLatitudeDelta(for: region),
                regionalMinLatitudeDelta: Self.regionalDotMinLatitudeDelta,
                streetMaxLatitudeDelta: Self.secondaryMarkerFullSizeMaxLatitudeDelta
            )
        )
    }

    init(route: SavedRouteDTO, onRouteSaved: ((SavedRouteDTO) -> Void)? = nil) {
        let routePoints = route.points.compactMap { dto -> RouteBuilderPoint? in
            guard let coordinate = Self.coordinate(from: dto) else { return nil }
            return RouteBuilderPoint(
                coordinate: coordinate,
                type: RouteBuilderPointType(rawValue: dto.markerType ?? "") ?? .path
            )
        }
        let roadCoordinates = route.roadCoordinates.compactMap(Self.coordinate(from:))
        let displayCoordinates = roadCoordinates.count >= 2 ? roadCoordinates : routePoints.map(\.coordinate)
        let center = Self.centerCoordinate(for: displayCoordinates)
        let region = RouteMapGeometry.regionToFit(displayCoordinates, fallback: center)
        let hasStart = routePoints.contains { $0.type == .start }
        let hasFinish = routePoints.contains { $0.type == .finish }
        let opensInEditMode = hasStart && hasFinish
        let waypointCount = routePoints.filter { $0.type == .waypoint }.count
        let inferredCheckpointSpacing = Self.inferredCheckpointSpacingMeters(
            checkpointCount: waypointCount,
            roadCoordinates: roadCoordinates
        )
        self.initialCenter = center
        self.onRouteSaved = onRouteSaved
        self.initialMapViewport = OttoMapboxCamera.viewport(for: region)
        _editingRouteId = State(initialValue: route.id)
        _mapRegionCache = State(initialValue: RouteBuilderMapRegionCache(region: region))
        _latestObservedCameraRegion = State(initialValue: region)
        _mapVisibleLatitudeDelta = State(initialValue: Self.markerLODLatitudeDelta(for: region))
        _mapMarkerLODTier = State(
            initialValue: RouteBuilderMapMarkerLODTier.from(
                latitudeDelta: Self.markerLODLatitudeDelta(for: region),
                regionalMinLatitudeDelta: Self.regionalDotMinLatitudeDelta,
                streetMaxLatitudeDelta: Self.secondaryMarkerFullSizeMaxLatitudeDelta
            )
        )
        _roadPolylineIndex = State(initialValue: nil)
        _routeName = State(initialValue: route.name)
        _points = State(initialValue: routePoints)
        _roadCoordinates = State(initialValue: roadCoordinates)
        _distanceMeters = State(initialValue: route.distanceMeters)
        _travelSeconds = State(initialValue: route.etaSeconds)
        _didSnapToRoad = State(initialValue: roadCoordinates.count >= 2)
        _activeCheckpointSpacingMeters = State(initialValue: inferredCheckpointSpacing)
        _isManualMode = State(initialValue: opensInEditMode)
        _isEditMode = State(initialValue: opensInEditMode)
        _editBaseline = State(
            initialValue: RouteEditBaseline.capture(
                name: route.name,
                points: routePoints,
                roadCoordinates: roadCoordinates,
                distanceMeters: route.distanceMeters,
                travelSeconds: route.etaSeconds
            )
        )
    }

    var body: some View {
        routeBuilderStackWithLifecycle
            .alert("Route Builder", isPresented: errorAlertBinding) {
                Button("OK", role: .cancel) {}
            } message: {
                Text(errorMessage ?? "")
            }
            .alert("Name This Route", isPresented: $isShowingRouteNamePrompt) {
                TextField("Route name", text: $routeNameDraft)
                Button("Cancel", role: .cancel) {}
                Button("Save") {
                    attemptSave(named: routeNameDraft)
                }
            } message: {
                Text("Give this route a name before saving.")
            }
            .confirmationDialog("Marker Actions", isPresented: markerActionDialogBinding, titleVisibility: .visible) {
                markerActionDialogActions
            } message: {
                if let point = markerActionPoint {
                    Text(markerActionMessage(for: point))
                }
            }
            .alert("Delete Route Item?", isPresented: deleteRouteItemAlertBinding) {
                Button("Cancel", role: .cancel) { routePointPendingDeleteID = nil }
                Button("Delete", role: .destructive) { deletePendingRoutePoint() }
            } message: {
                Text("Removing this item will update the route path, route order, and distance/time estimates.")
            }
            .alert("Discard Changes?", isPresented: $isShowingDiscardChangesAlert) {
                Button("Cancel", role: .cancel) {}
                Button("Discard", role: .destructive) { dismiss() }
            } message: {
                Text("Closing now will lose your unsaved route edits.")
            }
    }

    private var routeBuilderStackWithLifecycle: some View {
        routeBuilderMainStack
            .onAppear(perform: handleRouteBuilderAppear)
            .onChange(of: mapMarkerLODTier) { _, _ in syncPerfDiagnosticsSnapshot() }
            .onChange(of: points.count) { _, _ in syncPerfDiagnosticsSnapshot() }
            .onChange(of: locationService.authorizationStatus) { _, _ in
                if editingRouteId == nil { bootstrapNewRouteMapCamera() }
            }
            .onChange(of: locationService.lastLocation) { _, _ in
                applyNewRouteUserLocationIfAvailable()
            }
            .onChange(of: locationService.mapLocationDisplayTick) { _, _ in
                applyNewRouteUserLocationIfAvailable()
            }
            .onChange(of: routeBuilderPerfOverlayEnabled) { _, enabled in
                guard appState.canAccessInternalDebugTools else { return }
                if enabled {
                    perfDiagnostics.activate()
                } else {
                    perfDiagnostics.deactivate()
                }
            }
            .onDisappear {
                snapTask?.cancel()
                mapCameraDebounceTask?.cancel()
                polylineIndexTask?.cancel()
                autoPathBootstrapTask?.cancel()
                #if DEBUG
                print("[RouteBuilder] editor dismissed — Mapbox map will unmount")
                OttoRouteBuilderDebugLog.editorOpenEnded()
                #endif
                appState.setRouteBuilderPresented(false)
                perfDiagnostics.deactivate()
            }
    }

    private var routeBuilderMainStack: some View {
        ZStack {
            routeBuilderMapLayer
            routeBuilderChromeOverlay
        }
        .background { routeBuilderLayoutProbe }
        .background(Color.black)
        .overlay { routeBuilderPrimerOverlay }
    }

    @ViewBuilder
    private var routeBuilderPrimerOverlay: some View {
        ZStack {
            if showRouteEducation {
                routeBuilderEducationDialog
                    .transition(.opacity.combined(with: .scale(scale: 0.96)))
            }
            if showLocationPrimer {
                routeBuilderLocationPrimerDialog
                    .transition(.opacity.combined(with: .scale(scale: 0.96)))
            }
        }
    }

    @ViewBuilder
    private var markerActionDialogActions: some View {
        if let point = markerActionPoint {
            Button("Move \(point.displayTitle)") {
                movingPointID = point.id
                markerActionPointID = nil
                appState.activeToast = AppToast(
                    text: "Move the map under the crosshair, then tap Move Here",
                    systemImage: "scope"
                )
            }
            if point.type != .start && point.type != .finish {
                Button("Delete \(point.displayTitle)", role: .destructive) {
                    routePointPendingDeleteID = point.id
                    markerActionPointID = nil
                }
            }
            Button("Cancel", role: .cancel) { markerActionPointID = nil }
        }
    }

    private var errorAlertBinding: Binding<Bool> {
        Binding(get: { errorMessage != nil }, set: { if !$0 { errorMessage = nil } })
    }

    private var markerActionDialogBinding: Binding<Bool> {
        Binding(get: { markerActionPointID != nil }, set: { if !$0 { markerActionPointID = nil } })
    }

    private var deleteRouteItemAlertBinding: Binding<Bool> {
        Binding(get: { routePointPendingDeleteID != nil }, set: { if !$0 { routePointPendingDeleteID = nil } })
    }

    private func handleRouteBuilderAppear() {
        #if DEBUG
        OttoRouteBuilderDebugLog.editorOpenBegan(routeId: editingRouteId, roadPointCount: roadCoordinates.count)
        #endif
        appState.setRouteBuilderPresented(true)
        if showsRouteBuilderPerfOverlay {
            perfDiagnostics.activate()
        }
        syncPerfDiagnosticsSnapshot()
        if !appState.hasRoutesAccess {
            dismiss()
            return
        }
        captureInitialEditBaselineIfNeeded()
        if editingRouteId == nil {
            bootstrapNewRouteMapCamera()
        }
        presentRouteBuilderPrimersIfNeeded()
        refreshRoadPolylineCache()
    }

    private var showsRouteBuilderPerfOverlay: Bool {
        routeBuilderPerfOverlayEnabled && appState.canAccessInternalDebugTools
    }

    private var routeBuilderMapLayer: some View {
        RouteBuilderMapHost(
            initialViewport: initialMapViewport,
            programmaticViewport: programmaticMapViewport,
            mapContent: currentMapContent,
            diagnostics: perfDiagnostics,
            onCameraChanged: scheduleObservedCameraChange,
            onGestureEnded: { scheduleCameraSettle(force: true) },
            onLongPress: handleMapLongPress,
            onMarkerLongPress: handleRouteMarkerLongPress(id:)
        )
        .equatable()
        .ignoresSafeArea()
        .zIndex(0)
    }

    private var routeBuilderChromeOverlay: some View {
        ZStack {
            topScrim
                .zIndex(1)

            placementCrosshair(
                mapSize: mapContainerSize,
                visibleMapBottom: routeBuilderVisibleMapBottom
            )
            .zIndex(2)

            if showsRouteBuilderPerfOverlay {
                RouteBuilderBodyRebuildProbe(diagnostics: perfDiagnostics)
                    .frame(width: 0, height: 0)
                    .allowsHitTesting(false)
            }

            if showsRouteBuilderPerfOverlay {
                routeBuilderPerfOverlayLayer
            }

            VStack(spacing: 0) {
                topBar
                    .padding(.horizontal, 16)
                    .padding(.top, 10)
                    .allowsHitTesting(true)
                Spacer()
                    .allowsHitTesting(false)
                routeBuilderBottomBar(bottomSafeArea: mapBottomSafeArea)
                    .allowsHitTesting(true)
            }
            .ignoresSafeArea(edges: .bottom)
            .zIndex(3)

            if uiState != .generatingRoute {
                routeBuilderRecenterFabOverlay
                    .zIndex(4)
            }

            if uiState == .generatingRoute {
                RouteBuilderGeneratingOverlay()
                    .zIndex(5)
            }
        }
        .coordinateSpace(name: Self.routeBuilderOverlayCoordinateSpace)
        .onPreferenceChange(RouteBuilderBottomBarMinYKey.self) { minY in
            guard let minY, minY.isFinite, minY > 0 else { return }
            measuredBottomBarMinY = minY
        }
    }

    private var routeBuilderRecenterFabOverlay: some View {
        ZStack(alignment: .topLeading) {
            routeBuilderRecenterButton
                .position(routeBuilderRecenterFabCenter)
        }
        .frame(maxWidth: .infinity, maxHeight: .infinity, alignment: .topLeading)
    }

    private var routeBuilderPerfOverlayLayer: some View {
        VStack {
            HStack {
                RouteBuilderPerfOverlay(diagnostics: perfDiagnostics)
                    .padding(.leading, 10)
                    .padding(.top, 56)
                Spacer(minLength: 0)
            }
            Spacer()
        }
        .allowsHitTesting(false)
        .zIndex(5)
    }

    private var routeBuilderLayoutProbe: some View {
        GeometryReader { proxy in
            Color.clear
                .task(id: "\(proxy.size.width)x\(proxy.size.height)-\(proxy.safeAreaInsets.bottom)") {
                    mapContainerSize = proxy.size
                    mapBottomSafeArea = proxy.safeAreaInsets.bottom
                }
        }
    }

    private var currentMapContent: RouteBuilderMapContent {
        let line = routeLineCoordinates
        let pathDots = nativePathDotsForDisplay()
        return RouteBuilderMapContent(
            lineFingerprint: RoutePolylineDisplayOptimizer.fingerprint(line),
            lineCoordinates: line,
            markers: mapMarkerSnapshots(),
            nativePathDotsFingerprint: pathDots.fingerprint,
            nativePathDots: pathDots,
            allowsInteraction: !isMapInteractionDisabled
        )
    }

    private func syncPerfDiagnosticsSnapshot() {
        perfDiagnostics.updateSnapshot(
            roadPoints: roadCoordinates.count,
            displayPoints: routeLineCoordinates.count,
            markers: points.count,
            visibleMarkers: mapPointsForDisplay().count,
            lodTier: mapMarkerLODTier,
            latitudeDelta: mapRegionCache.region.span.latitudeDelta,
            lightweightRendering: false
        )
    }

    private func mapMarkerSnapshots() -> [RouteBuilderMapMarkerSnapshot] {
        mapPointsForDisplay().map { entry in
            let point = entry.point
            let presentation = markerPresentation(for: point)
            return RouteBuilderMapMarkerSnapshot(
                id: point.id,
                coordinate: point.coordinate,
                markerType: point.type.rawValue,
                isAutoShape: point.isAutoShape,
                presentation: presentation,
                pinScale: markerPinScale(for: point),
                dotColor: point.type.color,
                accessibilityTitle: point.displayTitle,
                refreshID: routePointMarkerRefreshID(for: point),
                originalIndex: entry.originalIndex
            )
        }
    }

    private var usesWideRegionalLightweightRendering: Bool {
        mapMarkerLODTier == .regional
            && mapRegionCache.region.span.latitudeDelta > Self.regionalDotMinLatitudeDelta * 12
    }

    private func mapPointsForDisplay() -> [(point: RouteBuilderPoint, originalIndex: Int)] {
        let indexed = Array(points.enumerated()).map { (point: $0.element, originalIndex: $0.offset) }
        if usesWideRegionalLightweightRendering {
            return indexed.filter { $0.point.type == .start || $0.point.type == .finish }
        }
        return indexed
    }

    private func nativePathDotsForDisplay() -> RouteBuilderNativePathDotsState {
        // Path markers render as view annotations (same LOD / pin scale as checkpoint and stop).
        RouteBuilderNativePathDotsState(fingerprint: "empty", userDots: [], autoDots: [])
    }

    private func handleRouteMarkerLongPress(id: UUID) {
        guard let point = points.first(where: { $0.id == id }) else { return }
        handleRouteMarkerLongPress(point)
    }

    private var topBar: some View {
        ZStack {
            Text(editingRouteId == nil ? "New Rally Route" : routeName)
                .font(.headline.weight(.semibold))
                .foregroundStyle(.white)
                .lineLimit(1)
                .padding(.horizontal, showsEditSessionUndo ? 148 : 86)
                .frame(maxWidth: .infinity)
                #if DEBUG
                .onLongPressGesture(minimumDuration: 0.8) {
                    routeBuilderPerfOverlayEnabled.toggle()
                }
                #endif

            HStack {
                Button {
                    attemptClose()
                } label: {
                    OttoHeaderIconButtonLabel(systemImage: "xmark")
                }

                Spacer()

                if showsEditSessionUndo {
                    Button {
                        undoEditSessionChange()
                    } label: {
                        OttoHeaderTextButtonLabel(
                            title: RouteBuilderCopy.undo,
                            isEnabled: canUndoEditSession && !isSaving && !isSnapping
                        )
                    }
                    .disabled(!canUndoEditSession || isSaving || isSnapping)
                }

                Button {
                    beginSaveFlow()
                } label: {
                    OttoHeaderTextButtonLabel(title: "Save", isEnabled: saveIsActive)
