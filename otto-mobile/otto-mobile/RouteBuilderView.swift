    private func applyMovedPoint(
        original: RouteBuilderPoint,
        index: Int,
        newCoordinate: CLLocationCoordinate2D
    ) {
        let activeDragPointID: UUID?

        if original.isAutoShape {
            let lockedPoints = points.filter { !$0.isAutoShape }
            let draggedShape = RouteBuilderPoint(
                coordinate: newCoordinate,
                type: .path,
                isAutoGenerated: true,
                id: original.id
            )
            points = lockedPoints + [draggedShape]
            if let projection = projectOntoRouteLine(newCoordinate) {
                points = orderRoutePoints(points, projectedPositions: [draggedShape.id: projection.arcLengthMeters])
            } else {
                points = orderRoutePoints(points)
            }
            activeDragPointID = draggedShape.id
        } else {
            let moved = RouteBuilderPoint(
                coordinate: newCoordinate,
                type: original.type,
                isAutoGenerated: original.isAutoGenerated,
                id: original.id
            )
            points[index] = moved
            if let projection = projectOntoRouteLine(newCoordinate) {
                points = orderRoutePoints(points, projectedPositions: [moved.id: projection.arcLengthMeters])
            } else {
                points = orderRoutePoints(points)
            }
            activeDragPointID = nil
        }

        self.movingPointID = nil
        if original.type == .start || original.type == .finish {
            prepareCheckpointRegenerationAfterEndpointChange()
        }
        if shouldRebuildRouteAfterPointMutation(for: original.type) {
            rebuildRoadPath(activeDragPointID: activeDragPointID)
        } else if original.type == .waypoint || original.type == .stop {
            points = orderRoutePoints(points)
        }
    }

    private func canDeleteRoutePoint(_ point: RouteBuilderPoint) -> Bool {
        point.type != .start && point.type != .finish
    }

    private func deletePendingRoutePoint() {
        guard let routePointPendingDeleteID,
              let point = points.first(where: { $0.id == routePointPendingDeleteID }),
              canDeleteRoutePoint(point) else {
            self.routePointPendingDeleteID = nil
            return
        }

        if isRouteEditSessionActive {
            recordEditUndoSnapshotBeforeChange()
        }

        points.removeAll { $0.id == routePointPendingDeleteID }
        if movingPointID == routePointPendingDeleteID {
            movingPointID = nil
        }
        markerActionPointID = nil
        self.routePointPendingDeleteID = nil
        if shouldRebuildRouteAfterPointMutation(for: point.type) {
            rebuildRoadPath()
        }
    }

    private func snappedCoordinateForPlacement(type: RouteBuilderPointType, raw: CLLocationCoordinate2D) -> CLLocationCoordinate2D {
        guard routeLineCoordinates.count >= 2 else { return raw }
        switch type {
        case .waypoint, .stop:
            return projectOntoRouteLine(raw)?.coordinate ?? raw
        case .start, .finish, .path:
            return raw
        }
    }

    private func placePoint(_ type: RouteBuilderPointType, triggerGuidedGeneration: Bool = false) {
        if isRouteEditSessionActive {
            recordEditUndoSnapshotBeforeChange()
        }
        let raw = currentPlacementCoordinate
        let coordinate = snappedCoordinateForPlacement(type: type, raw: raw)
        applyPlacedPoint(type, coordinate: coordinate, triggerGuidedGeneration: triggerGuidedGeneration)
    }

    private func applyPlacedPoint(
        _ type: RouteBuilderPointType,
        coordinate: CLLocationCoordinate2D,
        triggerGuidedGeneration: Bool
    ) {
        let point = RouteBuilderPoint(coordinate: coordinate, type: type)
        switch type {
        case .start:
            if let index = points.firstIndex(where: { $0.type == .start }) {
                points[index] = point
            } else {
                points.insert(point, at: 0)
            }
        case .finish:
            if let index = points.firstIndex(where: { $0.type == .finish }) {
                points[index] = point
            } else {
                points.append(point)
            }
        case .path, .waypoint, .stop:
            if routeLineCoordinates.count >= 2 {
                let projection = projectOntoRouteLine(point.coordinate)
                var nextPoints = points + [point]
                nextPoints = orderRoutePoints(
                    nextPoints,
                    projectedPositions: projection.map { [point.id: $0.arcLengthMeters] } ?? [:]
                )
                points = nextPoints
            } else if let finishIndex = points.firstIndex(where: { $0.type == .finish }) {
                points.insert(point, at: finishIndex)
            } else {
                points.append(point)
            }
        }
        if type == .start || type == .finish {
            prepareCheckpointRegenerationAfterEndpointChange()
        }
        if triggerGuidedGeneration {
            startGuidedRouteGeneration()
        } else if shouldRebuildRouteAfterPointMutation(for: type) {
            rebuildRoadPath()
        }
    }

    private func shouldRebuildRouteAfterPointMutation(for type: RouteBuilderPointType) -> Bool {
        switch type {
        case .start, .finish, .path:
            return true
        case .waypoint, .stop:
            return false
        }
    }

    private func orderRoutePoints(
        _ candidatePoints: [RouteBuilderPoint],
        projectedPositions: [UUID: Double] = [:]
    ) -> [RouteBuilderPoint] {
        guard candidatePoints.count >= 2, routeLineCoordinates.count >= 2 else {
            return candidatePoints
        }
        let start = candidatePoints.first { $0.type == .start }
        let finish = candidatePoints.first { $0.type == .finish }
        let interior = candidatePoints.filter { $0.type != .start && $0.type != .finish }
        let sortedInterior = interior.enumerated().sorted { lhs, rhs in
            let lhsPosition = projectedPositions[lhs.element.id]
                ?? projectOntoRouteLine(lhs.element.coordinate)?.arcLengthMeters
                ?? Double(lhs.offset)
            let rhsPosition = projectedPositions[rhs.element.id]
                ?? projectOntoRouteLine(rhs.element.coordinate)?.arcLengthMeters
                ?? Double(rhs.offset)
            if lhsPosition == rhsPosition {
                return lhs.offset < rhs.offset
            }
            return lhsPosition < rhsPosition
        }.map(\.element)
        return [start].compactMap { $0 } + sortedInterior + [finish].compactMap { $0 }
    }

    private func rebuildRoadPath(activeDragPointID: UUID? = nil) {
        snapTask?.cancel()
        let routingInput = routingCoordinates(activeDragPointID: activeDragPointID)
        guard routingInput.count >= 2 else {
            roadCoordinates = routingInput
            distanceMeters = 0
            travelSeconds = 0
            isSnapping = false
            didSnapToRoad = false
            lastSnapTurnCoordinates = []
            refreshRoadPolylineCache()
            if isRunningGuidedGeneration {
                isRunningGuidedGeneration = false
                isManualMode = true
            }
            pendingCheckpointRegeneration = nil
            return
        }
        roadCoordinates = []
        didSnapToRoad = false
        isSnapping = true
        snapTask = Task {
            let result = await RouteRoadSnapper.buildRoute(for: routingInput)
            guard !Task.isCancelled else { return }
            await MainActor.run {
                roadCoordinates = result.coordinates
                distanceMeters = result.distanceMeters
                travelSeconds = result.travelTimeSeconds
                didSnapToRoad = result.didSnapToRoad
                isSnapping = false
                lastSnapTurnCoordinates = result.turnManeuverCoordinates
                syncAutoPathPoints(turnCoordinates: result.turnManeuverCoordinates)
                refreshRoadPolylineCache()
                if isRunningGuidedGeneration {
                    handleGuidedGenerationComplete()
                } else {
                    applyPendingCheckpointRegenerationIfNeeded()
                }
            }
        }
    }

