    private func syncAutoPathPoints(turnCoordinates: [CLLocationCoordinate2D]) {
        guard roadCoordinates.count >= 2 else { return }
        let autoCoordinates = RouteAutoPathPointGenerator.autoTurnPathCoordinates(
            turnCoordinates: turnCoordinates,
            roadCoordinates: roadCoordinates,
            polylineIndex: roadPolylineIndex
        )
        let preserved = points.filter { !$0.isAutoShape }
        let autoPoints = autoCoordinates.map {
            RouteBuilderPoint(coordinate: $0, type: .path, isAutoGenerated: true)
        }
        points = orderRoutePoints(preserved + autoPoints)
    }

    private func bootstrapAutoPathPointsIfNeeded() {
        guard roadCoordinates.count >= 2, didSnapToRoad else { return }
        guard !points.contains(where: { $0.isAutoShape }) else { return }

        if editingRouteId != nil {
            #if DEBUG
            OttoRouteBuilderDebugLog.autoPathBootstrap(source: "polyline-local")
            #endif
            let turns = RoutePolylineTurnExtractor.turnCoordinates(along: roadCoordinates)
            lastSnapTurnCoordinates = turns
            syncAutoPathPoints(turnCoordinates: turns)
            return
        }

        let routingInput = routingCoordinates()
        guard routingInput.count >= 2 else { return }
        #if DEBUG
        OttoRouteBuilderDebugLog.autoPathBootstrap(source: "directions-api")
        #endif
        Task {
            let result = await RouteRoadSnapper.buildRoute(for: routingInput)
            guard !Task.isCancelled else { return }
            await MainActor.run {
                lastSnapTurnCoordinates = result.turnManeuverCoordinates
                syncAutoPathPoints(turnCoordinates: result.turnManeuverCoordinates)
            }
        }
    }

    private func beginSaveFlow() {
        guard validateSavePreconditions() else { return }
        continueSaveFlowAfterCheckpointDecision()
    }

    private func validateSavePreconditions() -> Bool {
        guard saveIsActive, !isSaving else {
            if isRunningGuidedGeneration {
                errorMessage = "Wait for route generation to finish, then try again."
            } else if isSnapping {
                errorMessage = "Wait for the route to finish snapping to the road, then try again."
            } else if points.count >= 2 && distanceMeters <= 0 {
                errorMessage = "Route distance is still calculating. Try again in a moment."
            } else if distanceMeters < Self.minimumRouteDriveDistanceMeters {
                errorMessage = "Route drive distance must be at least 1,000 feet."
            }
            return false
        }
        return true
    }

    private func continueSaveFlowAfterCheckpointDecision() {
        if editingRouteId == nil {
            let trimmedName = routeName.trimmingCharacters(in: .whitespacesAndNewlines)
            routeNameDraft = trimmedName == "New Route" ? "" : trimmedName
            isShowingRouteNamePrompt = true
        } else {
            Task { await saveRoute(named: routeName) }
        }
    }

    private func attemptSave(named draftName: String) {
        guard validateSavePreconditions() else { return }
        let trimmedName = draftName.trimmingCharacters(in: .whitespacesAndNewlines)
        guard !trimmedName.isEmpty else { return }
        Task { await saveRoute(named: trimmedName) }
    }

    private func saveRoute(named draftName: String) async {
        guard appState.hasRoutesAccess else {
            errorMessage = "Routes isn’t available for your account yet."
            return
        }
        guard saveIsActive, !isSaving else {
            if isSnapping {
                errorMessage = "Wait for the route to finish snapping to the road, then try again."
            } else if points.count >= 2 && distanceMeters <= 0 {
                errorMessage = "Route distance is still calculating. Try again in a moment."
            } else if distanceMeters < Self.minimumRouteDriveDistanceMeters {
                errorMessage = "Route drive distance must be at least 1,000 feet."
            }
            return
        }
        let trimmedName = draftName.trimmingCharacters(in: .whitespacesAndNewlines)
        guard !trimmedName.isEmpty else { return }
        let snapped = roadCoordinates.count >= 2 ? roadCoordinates : points.map(\.coordinate)
        let pointDTOs = points
            .filter { !$0.isAutoShape }
            .compactMap { point -> RoutePointDTO? in
                guard Self.coordinate(from: RoutePointDTO(lat: point.coordinate.latitude, lng: point.coordinate.longitude, markerType: point.type.rawValue)) != nil else {
                    return nil
                }
                return RoutePointDTO(
                    lat: point.coordinate.latitude,
                    lng: point.coordinate.longitude,
                    markerType: point.type.rawValue
                )
            }
        let roadDTOs = snapped.compactMap { coordinate -> RoutePointDTO? in
            guard Self.coordinate(from: RoutePointDTO(lat: coordinate.latitude, lng: coordinate.longitude, markerType: RouteBuilderPointType.path.rawValue)) != nil else {
                return nil
            }
            return RoutePointDTO(
                lat: coordinate.latitude,
                lng: coordinate.longitude,
                markerType: RouteBuilderPointType.path.rawValue
            )
        }
        guard pointDTOs.count >= 2 else {
            errorMessage = "Add a start and finish before saving."
            return
        }
        guard roadDTOs.count >= 2 else {
            errorMessage = "Route geometry is still calculating. Try again in a moment."
            return
        }
        isSaving = true
        routeName = trimmedName
        do {
            let savedRoute: SavedRouteDTO
            if let editingRouteId {
                savedRoute = try await APIClient.shared.updateRoute(
                    routeId: editingRouteId,
                    name: routeName,
                    points: pointDTOs,
                    roadCoordinates: roadDTOs,
                    distanceMeters: distanceMeters,
                    etaSeconds: travelSeconds
                )
            } else {
                savedRoute = try await APIClient.shared.createRoute(
                    name: routeName,
                    points: pointDTOs,
                    roadCoordinates: roadDTOs,
                    distanceMeters: distanceMeters,
                    etaSeconds: travelSeconds
                )
            }
            onRouteSaved?(savedRoute)
            if editingRouteId != nil {
                let savedPoints = savedRoute.points.compactMap { dto -> RouteBuilderPoint? in
                    guard let coordinate = Self.coordinate(from: dto),
                          let markerType = dto.markerType,
                          let type = RouteBuilderPointType(rawValue: markerType) else { return nil }
                    return RouteBuilderPoint(coordinate: coordinate, type: type)
                }
                let savedRoadCoordinates = savedRoute.roadCoordinates.compactMap { Self.coordinate(from: $0) }
                editBaseline = RouteEditBaseline.capture(
                    name: savedRoute.name,
                    points: savedPoints,
                    roadCoordinates: savedRoadCoordinates,
                    distanceMeters: savedRoute.distanceMeters,
                    travelSeconds: savedRoute.etaSeconds
                )
            }
            appState.activeToast = AppToast(text: "Route saved", systemImage: "checkmark.circle.fill")
            OttoAnalytics.logRouteSaved()
            resetEditUndoSession()
            dismiss()
        } catch {
            errorMessage = saveRouteErrorMessage(for: error)
        }
        isSaving = false
    }

    private func saveRouteErrorMessage(for error: Error) -> String {
        let ns = error as NSError
        if ns.domain == "OttoAPI" {
            let msg = (ns.userInfo[NSLocalizedDescriptionKey] as? String)?
                .trimmingCharacters(in: .whitespacesAndNewlines) ?? ""
            if ns.code == 403 {
                return msg.isEmpty ? "Routes isn’t available for your account yet." : msg
            }
            if !msg.isEmpty {
                return msg
            }
        }
        if let urlError = error as? URLError {
            switch urlError.code {
            case .notConnectedToInternet, .networkConnectionLost, .cannotConnectToHost, .timedOut:
                return "Couldn’t save this route. Check your connection and try again."
            default:
                break
            }
        }
        if error is DecodingError {
            return "Couldn’t save this route. The server returned an unexpected response."
        }
        return "Couldn’t save this route. Try again."
    }

