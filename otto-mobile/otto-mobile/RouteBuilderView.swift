    private func stepCheckpointDensity(direction: Int) {
        let options = sortedCheckpointSpacingOptions()
        guard options.count > 1 else { return }

        let currentIndex = currentCheckpointSpacingIndex(in: options)
        let nextIndex = currentIndex + direction
        guard options.indices.contains(nextIndex) else { return }

        let next = options[nextIndex]
        guard next.spacingMeters != options[currentIndex].spacingMeters else { return }
        replaceAutoCheckpoints(spacingMeters: next.spacingMeters)
        syncSelectedCheckpointDensity(for: next)
        RouteBuilderHaptics.buttonTap()
    }

    private func sortedCheckpointSpacingOptions() -> [RouteAutoCheckpointGenerator.IntervalOption] {
        RouteAutoCheckpointGenerator.viableIntervals(roadCoordinates: roadCoordinates)
            .sorted { $0.spacingMeters > $1.spacingMeters }
    }

    private func currentCheckpointSpacingIndex(
        in options: [RouteAutoCheckpointGenerator.IntervalOption]
    ) -> Int {
        if let activeCheckpointSpacingMeters,
           let index = options.firstIndex(where: { $0.spacingMeters == activeCheckpointSpacingMeters }) {
            return index
        }

        guard !options.isEmpty else { return 0 }
        let currentCount = checkpointCount
        return options.enumerated().min(by: { lhs, rhs in
            let lhsDelta = abs(lhs.element.checkpointCount - currentCount)
            let rhsDelta = abs(rhs.element.checkpointCount - currentCount)
            if lhsDelta != rhsDelta { return lhsDelta < rhsDelta }
            return lhs.offset < rhs.offset
        })?.offset ?? 0
    }

    private func canStepCheckpointDensity(direction: Int) -> Bool {
        let options = sortedCheckpointSpacingOptions()
        guard options.count > 1 else { return false }
        let nextIndex = currentCheckpointSpacingIndex(in: options) + direction
        guard options.indices.contains(nextIndex) else { return false }
        return options[nextIndex].spacingMeters != options[currentCheckpointSpacingIndex(in: options)].spacingMeters
    }

    private func syncSelectedCheckpointDensity(for interval: RouteAutoCheckpointGenerator.IntervalOption) {
        if let option = RouteAutoCheckpointGenerator.densityOption(
            for: .recommended,
            roadCoordinates: roadCoordinates,
            turnCount: currentTurnCount
        ), option.spacingMeters == interval.spacingMeters {
            selectedCheckpointDensity = .recommended
            return
        }

        let options = RouteAutoCheckpointGenerator.densityOptions(
            roadCoordinates: roadCoordinates,
            turnCount: currentTurnCount
        )
        selectedCheckpointDensity = options.first(where: { $0.spacingMeters == interval.spacingMeters })?.tier ?? .recommended
    }

    private func applyCheckpointsForDensity(_ tier: CheckpointDensityTier, recordUndo: Bool = true) {
        if let option = RouteAutoCheckpointGenerator.densityOption(
            for: tier,
            roadCoordinates: roadCoordinates,
            turnCount: currentTurnCount
        ) {
            replaceAutoCheckpoints(spacingMeters: option.spacingMeters, recordUndo: recordUndo)
            selectedCheckpointDensity = option.tier
            return
        }

        guard tier == .recommended,
              let recommended = RouteAutoCheckpointGenerator.recommendedDefaultInterval(
                roadCoordinates: roadCoordinates,
                turnCount: currentTurnCount
              ) else {
            return
        }

        replaceAutoCheckpoints(spacingMeters: recommended.spacingMeters, recordUndo: recordUndo)
        selectedCheckpointDensity = .recommended
    }

    private func replaceAutoCheckpoints(spacingMeters: Double, recordUndo: Bool = true) {
        guard roadCoordinates.count >= 2 else { return }

        let generated = RouteAutoCheckpointGenerator.generate(
            roadCoordinates: roadCoordinates,
            spacingMeters: spacingMeters
        )
        guard !generated.isEmpty else { return }

        if recordUndo, isRouteEditSessionActive {
            recordEditUndoSnapshotBeforeChange()
        }

        let newWaypoints = generated.map { RouteBuilderPoint(coordinate: $0, type: .waypoint) }
        withAnimation(.easeInOut(duration: 0.22)) {
            points = orderRoutePoints(points.filter { $0.type != .waypoint } + newWaypoints)
        }
        activeCheckpointSpacingMeters = spacingMeters
    }

    /// Strips stale checkpoint markers before endpoint moves; regeneration runs after the route re-snaps.
    private func prepareCheckpointRegenerationAfterEndpointChange() {
        let hadCheckpoints = checkpointCount > 0
        guard hadCheckpoints || activeCheckpointSpacingMeters != nil || hasCompletedGuidedGeneration else { return }

        var spacing = activeCheckpointSpacingMeters
        if spacing == nil, hadCheckpoints {
            spacing = inferredCheckpointSpacingMeters(
                checkpointCount: checkpointCount,
                roadCoordinates: roadCoordinates
            )
        }

        pendingCheckpointRegeneration = PendingCheckpointRegeneration(
            spacingMeters: spacing,
            densityTier: selectedCheckpointDensity
        )
        points.removeAll { $0.type == .waypoint }
    }

    private func applyPendingCheckpointRegenerationIfNeeded() {
        guard let pending = pendingCheckpointRegeneration else { return }
        pendingCheckpointRegeneration = nil
        guard roadCoordinates.count >= 2, didSnapToRoad else { return }

        if let spacing = pending.spacingMeters {
            regenerateCheckpointsPreservingSpacing(spacing, recordUndo: false)
            return
        }

        applyCheckpointsForDensity(pending.densityTier, recordUndo: false)
    }

    private func regenerateCheckpointsPreservingSpacing(_ spacing: Double, recordUndo: Bool) {
        guard roadCoordinates.count >= 2 else { return }
        let intervals = RouteAutoCheckpointGenerator.viableIntervals(roadCoordinates: roadCoordinates)
        guard !intervals.isEmpty else {
            activeCheckpointSpacingMeters = nil
            return
        }

        let spacingToUse: Double
        if intervals.contains(where: { $0.spacingMeters == spacing }) {
            spacingToUse = spacing
        } else if let nearest = intervals.min(by: {
            abs($0.spacingMeters - spacing) < abs($1.spacingMeters - spacing)
        }) {
            spacingToUse = nearest.spacingMeters
        } else {
            activeCheckpointSpacingMeters = nil
            return
        }

        replaceAutoCheckpoints(spacingMeters: spacingToUse, recordUndo: recordUndo)
        if let interval = intervals.first(where: { $0.spacingMeters == spacingToUse }) {
            syncSelectedCheckpointDensity(for: interval)
        }
    }

    private static func inferredCheckpointSpacingMeters(
        checkpointCount: Int,
        roadCoordinates: [CLLocationCoordinate2D]
    ) -> Double? {
        guard checkpointCount > 0, roadCoordinates.count >= 2 else { return nil }
        let intervals = RouteAutoCheckpointGenerator.viableIntervals(roadCoordinates: roadCoordinates)
        return intervals.min(by: {
            abs($0.checkpointCount - checkpointCount) < abs($1.checkpointCount - checkpointCount)
        })?.spacingMeters
    }

    private func inferredCheckpointSpacingMeters(
        checkpointCount: Int,
        roadCoordinates: [CLLocationCoordinate2D]
    ) -> Double? {
        Self.inferredCheckpointSpacingMeters(
            checkpointCount: checkpointCount,
            roadCoordinates: roadCoordinates
        )
    }

