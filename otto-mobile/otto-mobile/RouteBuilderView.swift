private enum RouteBuilderPointType: String {
    case start
    case path
    case waypoint
    case stop
    case finish

    var title: String {
        switch self {
        case .start: return "Start"
        case .path: return "Path Marker"
        case .waypoint: return "Checkpoint"
        case .stop: return "Stop"
        case .finish: return "Finish"
        }
    }

    var systemImage: String {
        switch self {
        case .start: return "play.fill"
        case .path: return RouteMapMarkerAsset.pathInnerSymbolName
        case .waypoint: return "flag.fill"
        case .stop: return "octagon.fill"
        case .finish: return "flag.checkered"
        }
    }

    var color: Color {
        switch self {
        case .start: return RouteMapMarkerColors.startButton
        case .path: return .purple
        case .waypoint: return .blue
        case .stop: return .red
        case .finish: return .white
        }
    }

    var assetName: String {
        switch self {
        case .start: return "map-route-start"
        case .path: return "map-route-point"
        case .waypoint: return "map-route-checkpoint"
        case .stop: return "map-route-stop"
        case .finish: return "map-route-finish"
        }
    }
}

private struct RouteBuilderPoint: Identifiable {
    let id: UUID
    let coordinate: CLLocationCoordinate2D
    let type: RouteBuilderPointType
    /// Client-local only: `true` = auto shape handle (disposable, not saved, not routing input except during drag).
    var isAutoGenerated: Bool

    init(
        coordinate: CLLocationCoordinate2D,
        type: RouteBuilderPointType,
        isAutoGenerated: Bool = false,
        id: UUID = UUID()
    ) {
        self.id = id
        self.coordinate = coordinate
        self.type = type
        self.isAutoGenerated = isAutoGenerated
    }

    var isAutoShape: Bool {
        type == .path && isAutoGenerated
    }

    var isLockedIntentional: Bool {
        !isAutoShape
    }

    func affectsRouting(activeDragPointID: UUID? = nil) -> Bool {
        switch type {
        case .waypoint, .stop:
            return false
        case .path:
            if isAutoGenerated {
                return id == activeDragPointID
            }
            return true
        case .start, .finish:
            return true
        }
    }

    var displayTitle: String {
        isAutoShape ? "Route Bend" : type.title
    }
}

private struct RouteEditBaseline: Equatable {
    let name: String
    let pointSignatures: [String]
    let roadSignatures: [String]
    let distanceMeters: Double
    let travelSeconds: TimeInterval

    static func capture(
        name: String,
        points: [RouteBuilderPoint],
        roadCoordinates: [CLLocationCoordinate2D],
        distanceMeters: Double,
        travelSeconds: TimeInterval
    ) -> RouteEditBaseline {
        RouteEditBaseline(
            name: name.trimmingCharacters(in: .whitespacesAndNewlines),
            pointSignatures: points.filter { !$0.isAutoShape }.map(pointSignature),
            roadSignatures: roadCoordinates.map(coordinateSignature),
            distanceMeters: roundedDistance(distanceMeters),
            travelSeconds: travelSeconds.rounded()
        )
    }

    private static func pointSignature(_ point: RouteBuilderPoint) -> String {
        "\(point.type.rawValue)|\(coordinateSignature(point.coordinate))"
    }

    private static func coordinateSignature(_ coordinate: CLLocationCoordinate2D) -> String {
        String(format: "%.5f,%.5f", coordinate.latitude, coordinate.longitude)
    }

    private static func roundedDistance(_ value: Double) -> Double {
        (value * 10).rounded() / 10
    }

    func pathChanged(from roadCoordinates: [CLLocationCoordinate2D]) -> Bool {
        roadCoordinates.map(Self.coordinateSignature) != roadSignatures
    }
}

private struct RouteBuilderEditSnapshot {
    let intentionalPoints: [RouteBuilderPoint]
    let roadCoordinates: [CLLocationCoordinate2D]
    let distanceMeters: Double
    let travelSeconds: TimeInterval
    let didSnapToRoad: Bool
    let lastSnapTurnCoordinates: [CLLocationCoordinate2D]
    let selectedCheckpointDensity: CheckpointDensityTier

    static func capture(
        points: [RouteBuilderPoint],
        roadCoordinates: [CLLocationCoordinate2D],
        distanceMeters: Double,
        travelSeconds: TimeInterval,
        didSnapToRoad: Bool,
        lastSnapTurnCoordinates: [CLLocationCoordinate2D],
        selectedCheckpointDensity: CheckpointDensityTier
    ) -> RouteBuilderEditSnapshot {
        RouteBuilderEditSnapshot(
            intentionalPoints: points.filter { !$0.isAutoShape },
            roadCoordinates: roadCoordinates,
            distanceMeters: distanceMeters,
            travelSeconds: travelSeconds,
            didSnapToRoad: didSnapToRoad,
            lastSnapTurnCoordinates: lastSnapTurnCoordinates,
            selectedCheckpointDensity: selectedCheckpointDensity
        )
    }
}

