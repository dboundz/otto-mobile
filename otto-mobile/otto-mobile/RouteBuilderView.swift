    }

    private var showsEditSessionUndo: Bool {
        uiState == .editRoute || uiState == .manualPlot
    }

    private var canUndoEditSession: Bool {
        !editUndoStack.isEmpty
    }

    private var isRouteEditSessionActive: Bool {
        showsEditSessionUndo
    }

    private var topScrim: some View {
        LinearGradient(
            colors: [.black.opacity(0.45), .black.opacity(0.16), .clear],
            startPoint: .top,
            endPoint: .bottom
        )
        .frame(height: 142)
        .frame(maxHeight: .infinity, alignment: .top)
        .ignoresSafeArea()
        .allowsHitTesting(false)
    }

    private func placementCrosshair(
        mapSize: CGSize,
        visibleMapBottom: CGFloat
    ) -> some View {
        let center = crosshairCenter(
            mapSize: mapSize,
            visibleMapBottom: visibleMapBottom
        )

        return RouteBuilderPlacementCrosshair()
            .position(center)
            .allowsHitTesting(false)
    }

    /// Map tab FAB gap above bottom chrome (`MapScreen.mapFabOverlayBottomPadding`).
    private static let mapFabOverlayBottomPadding: CGFloat = 18
    private static let mapFabHorizontalInset: CGFloat = 18
    private static let mapFabDiameter: CGFloat = 56
    private static let routeBuilderOverlayCoordinateSpace = "routeBuilderOverlay"

    private var routeBuilderVisibleMapBottom: CGFloat {
        if let measuredBottomBarMinY, measuredBottomBarMinY > 0 {
            return measuredBottomBarMinY
        }
        return max(0, mapContainerSize.height - estimatedBottomBarVisibleHeight(bottomSafeArea: mapBottomSafeArea))
    }

    private var routeBuilderRecenterFabCenter: CGPoint {
        let radius = Self.mapFabDiameter / 2
        return CGPoint(
            x: Self.mapFabHorizontalInset + radius,
            y: max(radius, routeBuilderVisibleMapBottom - Self.mapFabOverlayBottomPadding - radius)
        )
    }

    private func estimatedBottomBarVisibleHeight(bottomSafeArea: CGFloat) -> CGFloat {
        bottomBarContentHeight(for: uiState) + 12 + 14 + 18 + bottomSafeArea
    }

    private func bottomBarContentHeight(for state: RouteBuilderUIState) -> CGFloat {
        switch state {
        case .setStart: return 270
        case .setFinish: return 340
        case .generatingRoute: return 100
        case .routeReady: return 330
        case .manualPlot: return 400
        case .editRoute: return 420
        }
    }

    private var uiState: RouteBuilderUIState {
        RouteBuilderUIStateResolver.resolve(
            RouteBuilderUIStateResolver.Inputs(
                hasStart: hasStart,
