        if usesZoomAwareSubtleScale(for: point) {
            return zoomAwareMarkerPinScale()
        }
        return 1
    }

    private func zoomAwareMarkerPinScale() -> CGFloat {
        let span = mapVisibleLatitudeDelta
        let far = Self.regionalDotMinLatitudeDelta
        let close = Self.secondaryMarkerFullSizeMaxLatitudeDelta
        if span <= close { return 1 }
        let range = far - close
        guard range > 0 else { return Self.routeBuilderSubtleMarkerScale }
        let t = min(1, max(0, (far - span) / range))
        return Self.routeBuilderSubtleMarkerScale + t * (1 - Self.routeBuilderSubtleMarkerScale)
    }

    private func usesZoomAwareSubtleScale(for point: RouteBuilderPoint) -> Bool {
        point.type == .path || point.type == .waypoint || point.type == .stop
