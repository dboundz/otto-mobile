    private static func coordinateAtCrosshair(
        region: MKCoordinateRegion,
        mapSize: CGSize,
        crosshairCenter: CGPoint
    ) -> CLLocationCoordinate2D {
        guard mapSize.width > 0, mapSize.height > 0 else { return region.center }

        let mapCenter = CGPoint(x: mapSize.width / 2, y: mapSize.height / 2)
        let offsetX = crosshairCenter.x - mapCenter.x
        let offsetY = crosshairCenter.y - mapCenter.y
        let latOffset = -offsetY * (region.span.latitudeDelta / mapSize.height)
        let lngOffset = offsetX * (region.span.longitudeDelta / mapSize.width)

        return CLLocationCoordinate2D(
            latitude: region.center.latitude + latOffset,
            longitude: region.center.longitude + lngOffset
        )
    }

